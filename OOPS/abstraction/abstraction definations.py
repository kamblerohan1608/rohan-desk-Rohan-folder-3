# Abstraction class :
#       Abstraction class is the class having abstract methods and concrete methods.
# We cannot create instance of abstract class.



# Abstract Method :
#       Abstraction method is the method which is compulsary to all the classes which follows the 
# abstract class. In abstract method no implementation is done only declearation/defining is done.     




# Concrete method :
#       Concrete method is general method inside a abstract class.




# Interface :
#       The class having only abstract methods is called as interface.

